def _section_coupling(a_glance):
    md("""---

## 3. Two people: coupling

A single character can misread the world. Two characters can misread *each
other* — and that needs one new verb, `reads`, which creates a **coupling**. One
character reads another's *surface* (a face, a tone) — never their interior. You
can see someone's face; you cannot see their feeling.

```python
from soma.narrative import Story, anxious, guarded

story = Story("a_glance", span="8s", step="1s", about="being seen")
ida = story.character("Ida", temperament=anxious)
theo = story.character("Theo", temperament=guarded)

ida.reads(theo, "face", gain=0.9, lag="1s")     # she reads his face, a beat late
ida.appraises("theo_face", feeling="shame", when="theo_face > 4")
ida.narrates(voice={"shame": "He can see right through me."})

story.at("2s", theo.shows("face", 9))           # Theo's face does something
print(story.run())
```

`reads` gives Ida a new channel, `theo_face`, that tracks Theo's real face with a
`gain` (how strongly it registers) and a `lag` (how late). When his face rises,
the reading crosses her threshold, her loop fires, and she makes `shame` of it —
telling herself he can see through her.

Notice what is *not* here: `as_threat=True`. Ida does not go in expecting his face
to mean anything (her prior is a flat zero), so when it rises it is a genuine
surprise — a large error — and that is what makes the loop fire. Had we marked it
a threat, she would have *expected* the worst, her belief would already sit high,
and his real face would match it too closely to register. Small choices in how a
character is wired change what they can feel.
""")
    code(dashboard(a_glance(), width=74))
    md("""
The new **BETWEEN THEM** panel names the coupling: Theo's face becomes Ida's
sensation, one second late. The `couple` lines in the chronicle are that transfer
happening. And the NARRATOR vs GROUND TRUTH panel shows the familiar gap — she
says *He can see right through me* while the record simply says she felt shame.

Two things are worth pausing on. First, the coupling is one-directional here: Ida
reads Theo, but Theo is just a face — he has no interior of his own. (In §5.2 we
make it mutual, and the two readings feed each other into a spiral.) Second,
`shame` is *Ida's* invention. Nothing about Theo's face carries shame; she
manufactures it. That is what makes coupling dramatic rather than telepathic: the
reader supplies the meaning, and can be wrong.

### Prose

Any simulation can be rendered as free-indirect prose with `story.prose()`. It is
not generation — each sentence is a rendering of a logged event:
""")
    code(prose(a_glance()))
    md("""
*Her face was hot before she knew why* is the shame; *He can see right through me*
is the narrator; *It was not true* is the gap between them. The prose says nothing
the event log did not.
""")


