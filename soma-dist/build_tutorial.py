_ANSI = re.compile(r"\x1b\[[0-9;]*m")


def strip(s: str) -> str:
    return _ANSI.sub("", s)


def src_of(fn) -> str:
    """The real source of a build function, dedented, decorators/blank-trimmed."""
    s = inspect.getsource(fn)
    return s.rstrip("\n")


def dashboard(story, width=78) -> str:
    return strip(story.run(width=width)).rstrip("\n")


def portrait(story, width=78) -> str:
    return strip(story.characterize(width=width)).rstrip("\n")


def prose(story) -> str:
    return strip(story.prose()).rstrip("\n")


def soma_src(story) -> str:
    return story.source().rstrip("\n")


def findings(story) -> str:
    """The real sift() output, wrapped to page width with a hanging indent so no
    text is lost but nothing runs off the edge."""
    import textwrap
    out = []
    for f in story.sift():
        line = f"[{f.pattern}] {f.text}"
        out.append(textwrap.fill(line, width=78, subsequent_indent="    "))
    return "\n".join(out)


def perturb(module_name, dial) -> str:
    mod = __import__(module_name)
    return strip(mod.build().perturb(dial)).rstrip("\n")


